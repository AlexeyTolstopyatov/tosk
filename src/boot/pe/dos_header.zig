//! This module contains realmode DOS executable header
//! Portable executables still uses IMAGE_DOS_HEADER for backward compatibility
//! (actually running DOS program instead of protected-mode program)

/// The Mark Zbikowski executable header
pub const ImageDosHeader = extern struct {
    // Stamdard DOS header
    e_magic: u16,
    e_cblp: u16,
    e_cp: u16,
    e_crlc: u16,
    e_cparhdr: u16,
    e_minalloc: u16,
    e_maxalloc: u16,
    e_ss: u16,
    e_sp: u16,
    e_csum: u16,
    e_ip: u16,
    e_cs: u16,
    e_lfarlc: u16,
    e_ovno: u16,
    // Extended DOS header
    e_res: [4]u16,
    e_oemid: u16,
    e_oeminfo: u16,
    e_res2: [10]u16,
    e_lfanew: u32,
};