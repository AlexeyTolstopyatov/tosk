const std = @import("std");
const BootInfo = @import("../boot/boot_info.zig").BootInfo;

pub var console: Console = undefined;

pub const PixelFormat = enum {
    RGBA,
    BGRA,
};
pub const Color = enum(u32) {
    Red = 0xFFFF0000,
    Green = 0xFF00FF00,
    Blue = 0xFF0000FF,

};
/// Embedded console service. BootInfo, given by EFI bootloader
/// contains a data about screen. (frame buffer and screen resolution).
/// This is not traditional case, when monitor pointer locates device buffer @0xB8000,
/// 
/// EFI services already caught all devices pointers, save them and turn
/// CPU mode into LONG (64-bit mode). 
pub const Console = struct {
    framebuffer: [*]volatile u32,
    /// Inherits by UEFI boot services
    width: u32,
    /// Inhertis by UEFI boot services
    height: u32,
    pixel_format: PixelFormat,
    /// Mono raster font (8x16). Any differences are throwing exceptions
    /// 256 characters
    font: []const u8,
    /// 8
    font_width: u32,
    /// 16
    font_height: u32,
    cursor_x: u32,
    cursor_y: u32,
    /// Foreground (pixel format dependent)
    fg_color: u32,
    /// Background (pixel format dependent)
    bg_color: u32,
    /// How many lines was scrolled up
    scroll_y: u32,

    pub fn init(boot_info: *const BootInfo) !Console {
        // framebuffer = anyopaque@80000000
        const fmt = PixelFormat.RGBA;
        const font_data = installIBMRaster();

        return Console {
            .framebuffer = @ptrCast(@alignCast(boot_info.framebuffer_base)),
            .width = boot_info.framebuffer_width,
            .height = boot_info.framebuffer_height,
            .pixel_format = fmt,
            .font = font_data,
            .font_width = 8,
            .font_height = 16,
            .cursor_x = 0,
            .cursor_y = 0,
            .fg_color = 0xFFFFFFFF,
            .bg_color = 0xFF0000FF,
            .scroll_y = 0,
        };
    }

    /// Draw charater pixel-by-pixel
    inline fn drawCharacter(self: *Console, c: u8, x: u32, y: u32) void {
        if (c < 32) return; // Escape sequencies handles exactly here
        const font_offset = c * self.font_height;
        for (0..self.font_height) |urow| {
            const byte = self.font[font_offset + urow];
            for (0..self.font_width) |ucol| {
                const col: u3 = @intCast(ucol);
                const row: u32 = @intCast(urow); 
                if (((byte >> (7 - @as(u3, col))) & 1) != 0) {
                    self.setPixel(x + col, y + row, self.fg_color);
                } else {
                    self.setPixel(x + col, y + row, self.bg_color);
                }
            }
        }
    }
    inline fn setPixel(self: *Console, px: u32, py: u32, color: u32) void {
        if (px >= self.width or py >= self.height) return;
        const idx = py * self.width + px;
        // Convert colors. If target scheme is (alpha)BGR -> needed to apply 
        // little bit transformations. (replace Blue bits with Red).
        //      color & 0xFF000000 -> Alpha channel and nothing after it (color => AA______)
        //      color & 0x00FF0000 << 16 -> Select Only Red channed and move it right (color => 0x__..__BB)
        //      color & 0x0000FF00 -> Only Green channel selected (color => 0x____GG__)
        //      color & 0x000000FF << 16 -> Select blue channel and move it left (color => 0x__RR__..) 
        // And hold the fact that color 
        const val = switch (self.pixel_format) {
            .RGBA => color,
            .BGRA => ((color & 0xFF000000)) | // A
                     ((color & 0x00FF0000) >> 16) | // R -> B
                     ((color & 0x0000FF00)) |      // G -> G
                     ((color & 0x000000FF) << 16), // B -> R
        };
        self.framebuffer[idx] = val;
    }
    /// Output character in the video buffer
    pub inline fn putChar(self: *Console, ch: u8) void {
        switch (ch) {
            '\n' => {
                self.cursor_x = 0;
                self.cursor_y += self.font_height;
                if (self.cursor_y + self.font_height > self.height) {
                    self.scrollUp();
                }
            },
            '\r' => self.cursor_x = 0,
            '\t' => {
                const tab_width = 4 * self.font_width;
                self.cursor_x = ((self.cursor_x / tab_width) + 1) * tab_width;
                if (self.cursor_x >= self.width) {
                    self.cursor_x = 0;
                    self.cursor_y += self.font_height;
                }
            },
            else => {
                self.drawCharacter(ch, self.cursor_x, self.cursor_y);
                self.cursor_x += self.font_width;
                if (self.cursor_x + self.font_width > self.width) {
                    self.cursor_x = 0;
                    self.cursor_y += self.font_height;
                }
                if (self.cursor_y + self.font_height > self.height) {
                    self.scrollUp();
                }
            },
        }
    }
    /// Move screen containment at the one line upper
    fn scrollUp(self: *Console) void {
        const line_height = self.font_height;
        const bytes_per_pixel = 4;
        const row_bytes = self.width * bytes_per_pixel;

        // Copy up lines 1..(height-line_height)
        const copy_size = row_bytes * (self.height - line_height);
        const src = @as([*]volatile u8, @ptrCast(self.framebuffer)) + row_bytes * line_height;
        const dst = @as([*]volatile u8, @ptrCast(self.framebuffer));
        
        //@memcpy(dst, src);
        for (src, 0..copy_size) |byte, i| {
            dst[i] = byte;
        }

        // Fill last lines line_height with backrgound
        const fill_start = dst + row_bytes * (self.height - line_height);
        const fill_size = row_bytes * line_height;
        
        //@memset(fill_start, @as(u8, @truncate(self.bg_color >> 24)));
        for (0..fill_size) |i| {
            fill_start[i] = @as(u8, @truncate(self.bg_color >> 24));
        }

        self.cursor_y -= line_height;
    }
    /// Formatted output in the VGA
    pub fn printf(self: *Console, comptime fmt: []const u8, args: anytype) void {
        var buffer: [1024]u8 = undefined;
        const result = std.fmt.bufPrint(&buffer, fmt, args) catch unreachable;

        for (result) |ch| {
            self.putChar(ch);
        }
    }
    pub fn foreground(self: *Console, hex: u32) void {
        self.fg_color = hex;
    }
    pub fn background(self: *Console, hex: u32) void {
        self.bg_color = hex;
    }
    pub fn clear(self: *Console) void {
        for (0..self.width) |w| {
            for (0..self.height) |h| {
                self.drawCharacter(' ', @intCast(w), @intCast(h));
            }
        }
    }
    pub const Writer = std.Io.Writer(*Console, error{}, write);
    
    fn write(self: *Console, bytes: []const u8) error{}!usize {
        for (bytes) |b| self.putChar(b);
        return bytes.len;
    }

    pub fn writer(self: *Console) Writer {
        return .{ .context = self };
    }
};
